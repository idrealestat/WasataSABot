# ======================= دوال البوت =======================
async def start(update: Update, context):
    await update.message.reply_text("تفضل: هل لديك اي سؤال عقاري ؟")

async def handle_message(update: Update, context):
    user_message = update.message.text
    try:
        await context.bot.send_chat_action(chat_id=update.effective_chat.id, action="typing")
        response = client.chat.completions.create(
            model="llama-3.3-70b-versatile",  # نموذج Groq مجاني وقوي
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": user_message}
            ],
            temperature=0.3  # أقل إبداعاً لضمان الدقة
        )
        reply = response.choices[0].message.content
        await update.message.reply_text(reply)
    except Exception as e:
        await update.message.reply_text(f"حدث خطأ تقني: {e}")

# ======================= تشغيل البوت =======================
def main():
    app = Application.builder().token(TELEGRAM_TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))
    print("✅ البوت العقاري السعودي يعمل عبر Groq ...")
    app.run_polling()

if __name__ == "__main__":
    main()